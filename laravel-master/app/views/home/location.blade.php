		<h3 class="text-muted">Early History</h3>

		<div class="media">
			<a class="pull-left" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/early-history.png') }}">
			</a>
			<div class="media-body">
				<p>The history of <strong>Marrakech</strong> began in the mid-11th century when Berber tribesmen from the Western Sahara, called the <strong>Almoravids</strong> (the “veiled people”), moved north and established the town of <strong>Marrakech</strong> in 1062 as the economic and military base of a regime independent from both the <strong>Idrissid</strong> King in <strong>Fes</strong> and from the <strong>Abbasid</strong> Caliph in Bagdad.</p>
				<p>As the <strong>Almoravids</strong> extended their territorial control to include southern Spain and a large part of North Africa, <strong>Marrakech</strong> grew in size and importance. The growth continued with the reign of the <strong>Almohads</strong>, another group of Berber tribesmen took control of Morocco in 1150 and who kept <strong>Marrakech</strong> as the kingdom capital. <strong>Marrakech</strong> imperial status came to end in 1269, when the <strong>Marinids</strong> came to power and moved the capital to <strong>Fez</strong>.</p>
			</div>
		</div>

		<p>Today, <strong>Marrakech</strong> is a thriving metropolis of almost one million residents; a city that manages to offer all the amenities of modern life while maintaining the charm of its past. Its age old palm groves, its faded red ochre walls, its unique and exquisite architecture, its large and lively <strong>souk</strong>, its street performers, its sounds and aromas, remind the visitor that not much has changed in <strong>Marrakech</strong> since its early days.</p>

		<h3 class="text-muted">Things to see in Marrakech</h3>

		<ul>
  			<li><h4><em>Jamaa al Fna</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-right" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/jamaa-al-fna.png') }}">
			</a>
			<div class="media-body">
				<p><strong>Jamaa al Fna</strong> is <em>the</em> square in <strong>Marrakech</strong>, a large cultural space, located in <strong>Medina</strong> (<em>old city</em>), an open theater with a large number of concurrent spectacles. Singers, storytellers, snake charmers, monkey trainers, water vendors, healers, and acrobats entertain nightly audiences next to food stalls. The square is surrounded by several cafés from where one can marvel at this daily festival.</p>
			</div>
		</div>

		<ul>
  			<li><h4><em>Medina &amp; souks</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-left" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/medina-and-souks.png') }}">
			</a>
			<div class="media-body">
				<p>Like many Moroccan cities, <strong>Marrakech</strong> has its <strong><em>Medina</em></strong>, the old district surrounded by massive walls with several gates (<strong>babs</strong>). Two main entrances to <strong>Medina</strong> are behind the famous <strong>Jamaa al Fna</strong> square; and once you get in, you will discover what it is arguably Morocco’s largest and most renowned <strong>souk</strong>, literally thousands of merchants scattered along a labyrinth of narrow and congested allies. The <strong>Marrakech</strong> souk is essentially a life museum exhibiting Morocco’s cultural heritage. Anything that is made by hand and by local artisans, you’ll find it there and for a good price! But you need t engage in the local commercial culture, bartering.</p>
			</div>
		</div>
		
		<ul>
  			<li><h4><em>Koutoubia Mosque</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-right" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/koutoubia-mosque.png') }}">
			</a>
			<div class="media-body">
				<p>Also located near the <strong>Jamaa al Fna</strong> square, the <strong>Koutoubia Mosque</strong> is the largest mosque in Marrakech. Its construction began shortly after the <strong>Almohad</strong> conquest of Marrakesh around 1150. The mosque was built on the site of an 11th-century <strong>Almoravid</strong> mosque and was completed during the reign of the <strong>Almohad</strong> Sultan <strong>Yacoub el Mansour</strong> (1184-99). Its minaret is the oldest of the three great <strong>Almohad</strong> minarets remaining in the North Africa. The name <strong>Koutoubia</strong> comes from the Arabic word for “books” (<strong>koutoub</strong>) since the mosque location was adjacent to a nearby book market.</p>
			</div>
		</div>
		
		<ul>
  			<li><h4><em>Bahia Palace</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-left" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/bahia-palace.png') }}">
			</a>
			<div class="media-body">
				<p>A palace built in the late 19th century (between 1894 and 1900) <strong>Si Moussa</strong>, the son of a powerful aid to the <strong>King Muhammad IV</strong>, who wanted to build an astonishing living quarter in <strong>Marrakech</strong> inspired by Moroccan and Islamic architecture. <strong>Si Moussa</strong> commissioned the construction of the palace and named it after one of his wives, <strong>Bahia</strong>, which fittingly means in Arabic “Brilliance.”</p>
			</div>
		</div>
		
		<ul>
  			<li><h4><em>Menara Gardens</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-right" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/menara-gardens.png') }}">
			</a>
			<div class="media-body">
				<p>The <strong>Menara Gardens</strong> are located to the west of <strong>Marrakech</strong>. They consist essentially of a small pavilion, a man-made water reservoir (195 x 165 meters), surrounding olive groves, and orchards. The basin gets its water from the snow-capped Atlas Mountains in the background, some 30 km away, via an old gravity-flow hydraulic system. The basin then hoses the gardens through irrigation channels called <strong>qanat</strong>.</p>
				<p>To appreciate the magic of the <strong>Menara Gardens</strong>, you have to travel in time, to the 12th century, when the <strong>Almohad</strong> king <strong>Abd al-Mu’min</strong>, who in an attempt to mitigate <strong>Marrakech</strong>’s intensely hot summer days, decided to create an oasis.</p>
			</div>
		</div>

		<ul>
  			<li><h4><em>Majorelle Garden</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-left" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/majorelle-garden.png') }}">
			</a>
			<div class="media-body">
				<p><strong>Jacques Majorelle</strong>, a French painter born in Nancy (France) in 1886, came to <strong>Marrakech</strong> in 1919 to explore the region’s landscape and its impressively bright colors. He soon purchased a small acreage of land and made it home. Through time, <strong>Majorelle</strong> developed his acreage into an astonishingly beautiful installation of plants and colors an opened it to the public in 1947. Following a car accident, <strong>Majorelle</strong> returned to France, where he died in 1962. In 1980, Yves Saint Laurent and his partner, Pierre Bergé, acquired the garden and gave a second life. Today, the garden houses the Islamic Art Museum of Marrakech, whose collection includes North African textiles from Saint-Laurent's personal collection as well as ceramics, jewelry, and <strong>Majorelle</strong>’s paintings.</p>
			</div>
		</div>

		<ul>
  			<li><h4><em>Medersa Ben Youssef</em></h4></li>
		</ul>
		<div class="media">
			<a class="pull-right" href="#">
				<img class="media-object" src="{{ asset('assets/img/location/medersa-ben-youssef.png') }}">
			</a>
			<div class="media-body">
				<p>The Arabic term <strong>medersa</strong> is often used to refer to a pre-school or lower elementary school where pupils learn to memorize the Koran. <strong>Medersa Ben Youssef</strong> is an architecturally distinctive school initially built by a <strong>Marinid</strong> King, <strong>Abou el Hassan</strong>, in the 14th century and later rebuilt by a <strong>Saadi</strong> king in the 1560’s who embellish it with carved cedar wood, exquisite stuccowork, and colorful tiles called <strong>zellij</strong>.</p>
